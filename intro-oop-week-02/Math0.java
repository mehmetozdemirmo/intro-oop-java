public class Math0 {
    public static void main(String[] args){
        int var; //integer valiable
        double x; // floating variable
        var=10;
        x=10;

        System.out.println("original var:" + var);
        System.out.println("original x:" + x );
        System.out.println();

        var=var/6;
        x=x/4;

        System.out.println("after var:" + var);
        System.out.println("after x:" + x);
    }


}
